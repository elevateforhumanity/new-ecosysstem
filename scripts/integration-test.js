#!/usr/bin/env node

import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

class IntegrationTester {
  constructor() {
    this.results = {
      passed: 0,
      failed: 0,
      warnings: 0,
      tests: []
    };
  }

  async runAllTests() {
    console.log('üß™ Starting Complete Integration Tests');
    console.log('=' .repeat(60));

    // Test file structure
    await this.testFileStructure();

    // Test configuration files
    await this.testConfigurationFiles();

    // Test build artifacts
    await this.testBuildArtifacts();

    // Test scripts
    await this.testScripts();

    // Test workflows
    await this.testWorkflows();

    // Display results
    this.displayResults();

    return {
      success: this.results.failed === 0,
      ...this.results
    };
  }

  async testFileStructure() {
    console.log('\nüìÅ Testing file structure...');

    const requiredFiles = [
      'package.json',
      'netlify.toml',
      '.env.development',
      '.env.staging',
      '.env.production',
      'supabase/config.toml',
      'supabase/schema.sql',
      'scripts/generate-sitemap.js',
      'scripts/sitemap-automation.js',
      'scripts/validate-environment.js',
      'docs/environment-setup.md'
    ];

    const requiredDirectories = [
      'netlify/functions',
      'supabase/functions',
      'vite-react-supabase-app',
      '.github/workflows',
      'data/seeds'
    ];

    for (const file of requiredFiles) {
      await this.testFileExists(file);
    }

    for (const dir of requiredDirectories) {
      await this.testDirectoryExists(dir);
    }
  }

  async testConfigurationFiles() {
    console.log('\n‚öôÔ∏è Testing configuration files...');

    // Test package.json
    await this.testPackageJson();

    // Test netlify.toml
    await this.testNetlifyConfig();

    // Test Supabase config
    await this.testSupabaseConfig();

    // Test environment files
    await this.testEnvironmentFiles();
  }

  async testBuildArtifacts() {
    console.log('\nüèóÔ∏è Testing build artifacts...');

    // Test sitemap generation
    await this.testSitemapGeneration();

    // Test Vite build
    await this.testViteBuild();
  }

  async testScripts() {
    console.log('\nüìú Testing scripts...');

    const scripts = [
      'scripts/generate-sitemap.js',
      'scripts/sitemap-automation.js',
      'scripts/validate-environment.js'
    ];

    for (const script of scripts) {
      await this.testScriptExecutable(script);
    }
  }

  async testWorkflows() {
    console.log('\nüîÑ Testing GitHub workflows...');

    const workflows = [
      '.github/workflows/deploy-production.yml',
      '.github/workflows/data-sync.yml',
      '.github/workflows/health-check.yml',
      '.github/workflows/sitemap-generation.yml'
    ];

    for (const workflow of workflows) {
      await this.testWorkflowSyntax(workflow);
    }
  }

  async testFileExists(filePath) {
    try {
      await fs.access(filePath);
      this.pass(`‚úÖ File exists: ${filePath}`);
    } catch (error) {
      this.fail(`‚ùå File missing: ${filePath}`);
    }
  }

  async testDirectoryExists(dirPath) {
    try {
      const stats = await fs.stat(dirPath);
      if (stats.isDirectory()) {
        this.pass(`‚úÖ Directory exists: ${dirPath}`);
      } else {
        this.fail(`‚ùå Path exists but is not a directory: ${dirPath}`);
      }
    } catch (error) {
      this.fail(`‚ùå Directory missing: ${dirPath}`);
    }
  }

  async testPackageJson() {
    try {
      const content = await fs.readFile('package.json', 'utf8');
      const pkg = JSON.parse(content);

      if (pkg.name) {
        this.pass('‚úÖ package.json has name field');
      } else {
        this.fail('‚ùå package.json missing name field');
      }

      if (pkg.type === 'module') {
        this.pass('‚úÖ package.json configured as ES module');
      } else {
        this.warn('‚ö†Ô∏è package.json not configured as ES module');
      }

      if (pkg.scripts) {
        this.pass('‚úÖ package.json has scripts');
      } else {
        this.warn('‚ö†Ô∏è package.json missing scripts');
      }

    } catch (error) {
      this.fail(`‚ùå package.json invalid: ${error.message}`);
    }
  }

  async testNetlifyConfig() {
    try {
      const content = await fs.readFile('netlify.toml', 'utf8');
      
      if (content.includes('[build]')) {
        this.pass('‚úÖ netlify.toml has build configuration');
      } else {
        this.fail('‚ùå netlify.toml missing build configuration');
      }

      if (content.includes('[[redirects]]')) {
        this.pass('‚úÖ netlify.toml has redirects configuration');
      } else {
        this.warn('‚ö†Ô∏è netlify.toml missing redirects configuration');
      }

      if (content.includes('functions = "netlify/functions"')) {
        this.pass('‚úÖ netlify.toml has functions configuration');
      } else {
        this.warn('‚ö†Ô∏è netlify.toml missing functions configuration');
      }

    } catch (error) {
      this.fail(`‚ùå netlify.toml invalid: ${error.message}`);
    }
  }

  async testSupabaseConfig() {
    try {
      const content = await fs.readFile('supabase/config.toml', 'utf8');
      
      if (content.includes('project_id')) {
        this.pass('‚úÖ Supabase config has project_id');
      } else {
        this.fail('‚ùå Supabase config missing project_id');
      }

      if (content.includes('[edge_functions]')) {
        this.pass('‚úÖ Supabase config has edge_functions section');
      } else {
        this.warn('‚ö†Ô∏è Supabase config missing edge_functions section');
      }

    } catch (error) {
      this.fail(`‚ùå Supabase config invalid: ${error.message}`);
    }
  }

  async testEnvironmentFiles() {
    const envFiles = ['.env.development', '.env.staging', '.env.production'];

    for (const envFile of envFiles) {
      try {
        const content = await fs.readFile(envFile, 'utf8');
        
        if (content.includes('NODE_ENV=')) {
          this.pass(`‚úÖ ${envFile} has NODE_ENV`);
        } else {
          this.fail(`‚ùå ${envFile} missing NODE_ENV`);
        }

        if (content.includes('BASE_URL=')) {
          this.pass(`‚úÖ ${envFile} has BASE_URL`);
        } else {
          this.fail(`‚ùå ${envFile} missing BASE_URL`);
        }

      } catch (error) {
        this.fail(`‚ùå ${envFile} invalid: ${error.message}`);
      }
    }
  }

  async testSitemapGeneration() {
    try {
      // Check if sitemaps were generated
      await fs.access('deploy/sitemap.xml');
      this.pass('‚úÖ Sitemap index generated');

      await fs.access('deploy/sitemap-main.xml');
      this.pass('‚úÖ Main sitemap generated');

      // Check for program sitemaps
      const deployDir = await fs.readdir('deploy');
      const programSitemaps = deployDir.filter(file => file.startsWith('sitemap-programs-'));
      
      if (programSitemaps.length > 0) {
        this.pass(`‚úÖ Program sitemaps generated (${programSitemaps.length})`);
      } else {
        this.warn('‚ö†Ô∏è No program sitemaps found');
      }

    } catch (error) {
      this.fail(`‚ùå Sitemap generation test failed: ${error.message}`);
    }
  }

  async testViteBuild() {
    try {
      await fs.access('vite-react-supabase-app/dist/index.html');
      this.pass('‚úÖ Vite build output exists');

      const distDir = await fs.readdir('vite-react-supabase-app/dist');
      const hasAssets = distDir.some(file => file.startsWith('assets'));
      
      if (hasAssets) {
        this.pass('‚úÖ Vite build has assets');
      } else {
        this.warn('‚ö†Ô∏è Vite build missing assets directory');
      }

    } catch (error) {
      this.fail(`‚ùå Vite build test failed: ${error.message}`);
    }
  }

  async testScriptExecutable(scriptPath) {
    try {
      const stats = await fs.stat(scriptPath);
      
      // Check if file is executable (Unix permissions)
      if (stats.mode & parseInt('111', 8)) {
        this.pass(`‚úÖ Script is executable: ${scriptPath}`);
      } else {
        this.warn(`‚ö†Ô∏è Script not executable: ${scriptPath}`);
      }

      // Check if script has proper shebang
      const content = await fs.readFile(scriptPath, 'utf8');
      if (content.startsWith('#!/usr/bin/env node')) {
        this.pass(`‚úÖ Script has proper shebang: ${scriptPath}`);
      } else {
        this.warn(`‚ö†Ô∏è Script missing shebang: ${scriptPath}`);
      }

    } catch (error) {
      this.fail(`‚ùå Script test failed for ${scriptPath}: ${error.message}`);
    }
  }

  async testWorkflowSyntax(workflowPath) {
    try {
      const content = await fs.readFile(workflowPath, 'utf8');
      
      if (content.includes('name:')) {
        this.pass(`‚úÖ Workflow has name: ${workflowPath}`);
      } else {
        this.fail(`‚ùå Workflow missing name: ${workflowPath}`);
      }

      if (content.includes('on:')) {
        this.pass(`‚úÖ Workflow has triggers: ${workflowPath}`);
      } else {
        this.fail(`‚ùå Workflow missing triggers: ${workflowPath}`);
      }

      if (content.includes('jobs:')) {
        this.pass(`‚úÖ Workflow has jobs: ${workflowPath}`);
      } else {
        this.fail(`‚ùå Workflow missing jobs: ${workflowPath}`);
      }

    } catch (error) {
      this.fail(`‚ùå Workflow test failed for ${workflowPath}: ${error.message}`);
    }
  }

  pass(message) {
    this.results.passed++;
    this.results.tests.push({ type: 'pass', message });
    console.log(message);
  }

  fail(message) {
    this.results.failed++;
    this.results.tests.push({ type: 'fail', message });
    console.log(message);
  }

  warn(message) {
    this.results.warnings++;
    this.results.tests.push({ type: 'warn', message });
    console.log(message);
  }

  displayResults() {
    console.log('\n' + '='.repeat(60));
    console.log('üìä INTEGRATION TEST RESULTS');
    console.log('='.repeat(60));

    console.log(`‚úÖ Passed: ${this.results.passed}`);
    console.log(`‚ùå Failed: ${this.results.failed}`);
    console.log(`‚ö†Ô∏è Warnings: ${this.results.warnings}`);
    console.log(`üìã Total: ${this.results.tests.length}`);

    console.log('\n' + '='.repeat(60));
    
    if (this.results.failed === 0) {
      console.log('üéâ All integration tests passed!');
      if (this.results.warnings > 0) {
        console.log(`‚ö†Ô∏è ${this.results.warnings} warning(s) found - review recommended`);
      }
    } else {
      console.log(`üí• ${this.results.failed} test(s) failed - integration incomplete`);
    }
    
    console.log('='.repeat(60));
  }
}

// CLI execution
if (import.meta.url === `file://${process.argv[1]}`) {
  const tester = new IntegrationTester();
  
  tester.runAllTests()
    .then(result => {
      process.exit(result.success ? 0 : 1);
    })
    .catch(error => {
      console.error('üí• Integration test failed:', error.message);
      process.exit(1);
    });
}

export default IntegrationTester;